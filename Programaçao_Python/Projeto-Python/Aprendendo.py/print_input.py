#Aprendendo a usar input: serve para inserir dados dentro do código
nome = input("Report your name, please: ")
idade = input("Report your age: ")

#Aprendendo a usar end e sep, servem para mudar o inicio e o final de uma variável.
print(nome, idade)
print(nome, idade, end="...\n")
print(nome, idade, sep="#", end="...\n")
print(nome, idade, sep ="#")